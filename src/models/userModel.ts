import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      default: false,
    },
  },
  {
    timestamps: true,
  }
);

// delete old model
const UserModel = mongoose.model("User");
mongoose.deleteModel(UserModel.modelName);

// craete new model
module.exports = mongoose.model("User", userSchema);
